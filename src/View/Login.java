    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        LoginController login = new LoginController();
        try {

            if (this.jTextFieldUsuario.getText().contains("admin")) {
                String nome = login.loginAdm(this);
                if (nome != null) {
                    PrincipalAdm adm = new PrincipalAdm();
                    adm.getjLabelNomeUsuario().setText(nome.toUpperCase());
                    adm.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "USUÁRIO OU SENHA NÃO ENCONTRADO(A)", "LOGIN", 1);
                    this.getjTextFieldUsuario().setText("");
                    this.getjPasswordFieldSenha().setText("");
                    this.jTextFieldUsuario.requestFocus();
                }
            } else if (this.jTextFieldUsuario.getText() != null) {
                String nome = login.loginUsuario(this);
                if (nome != null) {
                    PrincipalUsuario usuario = new PrincipalUsuario();
                    usuario.getjLabelNomeUsuario().setText(nome.toUpperCase());
                    usuario.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "USUÁRIO OU SENHA NÃO ENCONTRADO(A)", "LOGIN", 1);
                    this.getjTextFieldUsuario().setText("");
                    this.getjPasswordFieldSenha().setText("");
                    this.jTextFieldUsuario.requestFocus();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

