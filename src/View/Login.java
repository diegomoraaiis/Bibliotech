    public boolean validaCampos() {
        String senha = new String(jPasswordFieldSenha.getPassword());
        if (senha.isBlank() || jTextFieldUsuario.getText().isBlank()) {
            JOptionPane.showMessageDialog(rootPane, " Campos em branco", "ERRO NO LOGIN", 2);
            jTextFieldUsuario.requestFocus();
            return true;
        }
        return false;
    }


